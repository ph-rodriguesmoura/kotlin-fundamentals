fun main() {
    val name = "Pedro Moura"

    // Função que muda o comportamento de um objeto (String)
    val newName = name.uppercase()

    // Acessa a propriedade de um objeto (String)
    val numberName = name.length

    println(newName)
    println(numberName)

}

