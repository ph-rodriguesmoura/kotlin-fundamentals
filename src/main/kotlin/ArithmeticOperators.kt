fun main() {
    val a = 10
    val b = 3

// Addition
    val soma = a + b  // Resultado: 13

// Subtraction
    val diferenca = a - b  // Resultado: 7

// Multiplication
    val produto = a * b  // Resultado: 30

// Division
    val quociente = a / b  // Resultado: 3

// Module
    val resto = a % b  // Resultado: 1
}

